#include <unistd.h>

void	ft_print_numbers(char c)
{
	while (c <= '9')
	{
		write(1, &c, 1);
		c++;
	}
}

int	main(void)
{
	char	d;
	
	d = '0';
	ft_print_numbers(d);
	return (0);
}
