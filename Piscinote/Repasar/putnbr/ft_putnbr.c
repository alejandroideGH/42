#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;
	
	c = 0;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	if (nb > 10)
	{
		ft_putnbr(nb/10);
	}
	c = (nb % 10) + '0';
	write (1, &c, 1);
}

int	main(void)
{
	ft_putnbr(425);
	write (1, "\n", 1);
	return (0);
}
